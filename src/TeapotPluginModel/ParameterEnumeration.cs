namespace TeapotPlugin.Model
{
    /// <summary>
    /// Parameter type
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Height of the teapot
        /// </summary>
        Height,

        /// <summary>
        /// The radius of the base of the teapot
        /// </summary>
        BaseCircle,

        /// <summary>
        /// The radius of the outer circle of the spout
        /// </summary>
        OuterSpoutCircle,

        /// <summary>
        /// The radius of the inner circle of the spout
        /// </summary>
        InnerSpoutCircle,

        /// <summary>
        /// Length of teapot spout
        /// </summary>
        SpoutLength,

        /// <summary>
        /// The radius of the circle for handle thickness
        /// </summary>
        HandleThickness,

        /// <summary>
        /// The shape of the handle
        /// </summary>
        HandleType,
    }
}
